    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        if (txtMaLoai.getText().equals("") || txtTenLoai.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập mã loại và tên loại", "Thông báo", JOptionPane.WARNING_MESSAGE);
        } else {

            if (txtMaLoai.getText().length() > 2 || txtTenLoai.getText().length() > 30) {
                JOptionPane.showMessageDialog(null, "Mã loại chỉ là 2 ký tự, tên loại chỉ 20 ký tự", "Thông báo", JOptionPane.WARNING_MESSAGE);
            } else {
                try {
                    int confirm = JOptionPane.showConfirmDialog(null, "Bạn có chắc muốn lưu không", "Thông báo", JOptionPane.YES_NO_OPTION);
                    if (confirm == JOptionPane.YES_OPTION) {
                        LoaiSP loaiSP = new LoaiSP(txtMaLoai.getText(), txtTenLoai.getText());
                        boolean check;
                        if (isUpdate == true) {
                            check = loaiSPController.AddLoaiSP(loaiSP);

                        } else {
                            check = loaiSPController.SuaLoaiSP(loaiSP);
                        }
                        if (check = true) {
                            JOptionPane.showMessageDialog(null, "Cập nhật thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                            //                defaultTableModel.setRowCount(0);
                            clearDataTable();
                            LoadDataTable();
                            ClearTextField();
                        }

                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Cập nhật thất bại", "Thông báo", JOptionPane.ERROR_MESSAGE);

                }
                setTextField(false);
                setButton(true);
            }
        }
    }//GEN-LAST:event_btnLuuActionPerformed

