    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        try {
            if (txtMaLoai.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Vui lòng chọn 1 sản phẩm để xóa", "Thông báo", JOptionPane.WARNING_MESSAGE);
            } else {
                if (txtMaLoai.getText().length() > 2 || txtTenLoai.getText().length() > 30) {
                    JOptionPane.showMessageDialog(null, "Mã loại chỉ là 2 ký tự, tên loại chỉ 20 ký tự", "Thông báo", JOptionPane.WARNING_MESSAGE);
                } else {
                    int confirm = JOptionPane.showConfirmDialog(null, "Bạn có chắc muốn xóa loại " + txtMaLoai.getText() + "không", "Thông báo", JOptionPane.YES_NO_OPTION);
                    if (confirm == JOptionPane.YES_OPTION) {
                        boolean check = loaiSPController.XoaLoaiSP(txtMaLoai.getText());
                        if (check == true) {
                            JOptionPane.showMessageDialog(null, "Xóa thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
//                defaultTableModel.setRowCount(0);
                            clearDataTable();
                            LoadDataTable();
                            ClearTextField();

                        }
                    }
                }

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Xóa thất bại", "Thông báo", JOptionPane.ERROR_MESSAGE);

        }

    }//GEN-LAST:event_btnXoaActionPerformed

