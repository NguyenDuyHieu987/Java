    void loadMANV()
    {
        NhanvienUse use = new NhanvienUse();
        List<NhanvienObject> hd = use.getALL();
        MANVCB.removeAllItems();
        for (NhanvienObject nvOb : hd) {
            MANVCB.addItem(nvOb.getManv());
        }
    }
    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        NhanvienObject nv = new NhanvienObject();
        nv.setManv(MANVTF.getText());
        nv.setTennv(TENNVTF.getText());
        nv.setDiachi(DIACHITF.getText());
        String gioitinh = "";
        if(NAMCHECK.isSelected())
            gioitinh = "NAM";
        if(NUCHECK.isSelected())
            gioitinh = "NỮ";
        nv.setGioitinh(gioitinh);
        nv.setSdt(SDTTF.getText());
        DateFormat Date = new SimpleDateFormat("yyyy-MM-dd");
        nv.setNgaysinh(Date.format(NgaysinhPicker.getDate()));
        nv.setEmail(EMAILTF.getText());
        nv.setSocmnd(SOCMNDTF.getText());
        if(NVUse.CheckADD(MANVTF.getText()) == 1)
        {
            JOptionPane.showMessageDialog(this, "Mã nhân viên '"+MANVTF.getText()+"' đã tồn tại","Lỗi", JOptionPane.ERROR_MESSAGE);
        }
        else{
            JOptionPane.showMessageDialog(this, "Thêm thành công","Thông báo", JOptionPane.INFORMATION_MESSAGE);
            NVUse.ThemNV(nv);
            defaultTableModel.setRowCount(0);
            SetData(NVUse.getALL());
            ClearText();
        }
    }//GEN-LAST:event_btnThemActionPerformed

