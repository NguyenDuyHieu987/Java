    void ClearText()
    {
        MANVTF.setText("");
        TENNVTF.setText("");
        NAMCHECK.setSelected(true);
        DIACHITF.setText("");
        SDTTF.setText("");
        DateFormat Date = new SimpleDateFormat("yyyy-MM-dd");
        try {
            NgaysinhPicker.setDate(Date.parse("2021-1-1"));
        } catch (ParseException ex) {
            Logger.getLogger(GDNhanvien.class.getName()).log(Level.SEVERE, null, ex);
        }
        EMAILTF.setText("");
        SOCMNDTF.setText("");
    }
