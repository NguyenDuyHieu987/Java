    private void btnTimkiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimkiemActionPerformed
        // TODO add your handling code here:
        String manv = String.valueOf(MANVCB.getSelectedItem());
        NhanvienObject nv = NVUse.getOne(manv);
        if(nv.getDiachi() == null || nv.getEmail() == null || nv.getGioitinh() == null || nv.getManv() == null || nv.getSdt() == null)
        {
            JOptionPane.showMessageDialog(this, "KHÔNG CÓ NHÂN VIÊN MÃ TRÊN","LỖI",JOptionPane.ERROR_MESSAGE);
        }else{
            defaultTableModel.setRowCount(0);
            defaultTableModel.addRow(new Object[]{
                    nv.getManv(),nv.getTennv(),nv.getGioitinh(),nv.getDiachi(),
                    nv.getSdt(),nv.getNgaysinh(),nv.getEmail(),nv.getSocmnd()
                });
        }
        
    }//GEN-LAST:event_btnTimkiemActionPerformed

    private void NVTABLEMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_NVTABLEMouseClicked
        // TODO add your handling code here:
        int row = NVTABLE.getSelectedRow();
        if(row == -1)
        {
            JOptionPane.showMessageDialog(this, "VUI LÒNG CHỌN DÒNG CẦN SỬA","LỖI",JOptionPane.ERROR_MESSAGE);
        }
        else
        {
            String manv  = String.valueOf(NVTABLE.getValueAt(row, 0));
            NhanvienObject nv = NVUse.getOne(manv);
            MANVTF.setText(nv.getManv());
            TENNVTF.setText(nv.getTennv());
            String gioitinh = String.valueOf(nv.getGioitinh());
            if(gioitinh.equals("NAM"))
            {
                 NAMCHECK.setSelected(true);
            }
            else if(gioitinh.equals("NỮ"))
            {
                NUCHECK.setSelected(true);
            }
            DIACHITF.setText(nv.getDiachi());
            SDTTF.setText(nv.getSdt());
            DateFormat Date = new SimpleDateFormat("yyyy-MM-dd");
            try {
                NgaysinhPicker.setDate(Date.parse(nv.getNgaysinh()));
            } catch (ParseException ex) {
                Logger.getLogger(GDNhanvien.class.getName()).log(Level.SEVERE, null, ex);
            }
            EMAILTF.setText(nv.getEmail());
            SOCMNDTF.setText(nv.getSocmnd());
            btnOk.setEnabled(true);
            btnThem.setEnabled(false);
            btnSua.setEnabled(true);
            btnXoa.setEnabled(true);
        }
    }//GEN-LAST:event_NVTABLEMouseClicked


