    private void TBThanhvienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TBThanhvienMouseClicked
        // TODO add your handling code here:
        int row = TBThanhvien.getSelectedRow();
        if(row == -1)
        {
            JOptionPane.showMessageDialog(this, "VUI LÒNG CHỌN DÒNG","LỖI",JOptionPane.ERROR_MESSAGE);
        }
        else{
            String matv = String.valueOf(TBThanhvien.getValueAt(row, 0));
            ThanhvienObject tv = thanhviensevice.getOne(matv);
            MATVTF.setText(tv.getMatv());
            HOTENTF.setText(tv.getTentv());
            SDTTF.setText(tv.getSdt());
            DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            try {
                DatePIcker.setDate(dateFormat.parse(tv.getNgaysinh()));
            } catch (ParseException ex) {
                Logger.getLogger(GDQLThanhvien.class.getName()).log(Level.SEVERE, null, ex);
            }
            MAHGDTF.setText(tv.getMahgd());
            SOCMNDTF.setText(tv.getSocmnd());
            EMAILTF.setText(tv.getEmail());
            if(tv.getGioitinh().equals("NAM"))
                NAMCHECK.setSelected(true);
             if(tv.getGioitinh().equals("NỮ"))
                NUCHECK.setSelected(true);
            btnOK.setEnabled(true);
            btnThem.setEnabled(false);
            btnSua.setEnabled(true);
            btnXoa.setEnabled(true);
        }
    }//GEN-LAST:event_TBThanhvienMouseClicked


