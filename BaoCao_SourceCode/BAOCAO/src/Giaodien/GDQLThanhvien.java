    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        ThanhvienObject tv = new ThanhvienObject();
        tv.setMatv(MATVTF.getText());
        tv.setTentv(HOTENTF.getText());
        tv.setSdt(SDTTF.getText());
        tv.setMahgd(MAHGDTF.getText());
        tv.setSocmnd(SOCMNDTF.getText());
        String gioitinh ="";
        if(NAMCHECK.isSelected())
            gioitinh = "NAM";
        if(NUCHECK.isSelected())
            gioitinh = "NỮ";
        tv.setGioitinh(gioitinh);
        tv.setEmail(EMAILTF.getText());
        DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String date = dateFormat.format(DatePIcker.getDate());
        tv.setNgaysinh(String.valueOf(date));
        
        thanhviensevice.updateTV(tv);
        defaultTableModel.setRowCount(0);
        setDATA(thanhviensevice.getALL());
        ClearText();
        
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnTimkiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimkiemActionPerformed
        // TODO add your handling code here:
        String matv = String.valueOf(MATVCB.getSelectedItem());
        ThanhvienObject tv = thanhviensevice.getOneByID(matv);
        if(tv == null)
        {
            JOptionPane.showMessageDialog(this, "Không có thành viên trên ","LỖI ",JOptionPane.ERROR_MESSAGE);
        }
        else
        {
            defaultTableModel.setRowCount(0);
            defaultTableModel.addRow(new Object[]{tv.getMatv(),tv.getTentv(),tv.getSdt(),tv.getNgaysinh(),tv.getMahgd(),tv.getSdt(),tv.getEmail(),tv.getGioitinh()});
        }
    }//GEN-LAST:event_btnTimkiemActionPerformed

