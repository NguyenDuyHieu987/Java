    private void TKTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TKTableMouseClicked
        // TODO add your handling code here:
        int row = TKTable.getSelectedRow();
        if(row == -1)
        {
            JOptionPane.showMessageDialog(this, "VUI LÒNG CHỌN DÒNG CẦN XÓA ","LỖI ",JOptionPane.ERROR_MESSAGE);
        }
        else{
            int ID = Integer.valueOf( String.valueOf(TKTable.getValueAt(row, 0)));
            TaikhoanObject tk = TKUse.getOne(ID);
            Manvtf.setText(String.valueOf(Integer.valueOf(tk.getID())));
            String quyen = String.valueOf(tk.getQuyen());
            quyencb.setSelectedItem(quyen);
            tendangnhaptf.setText(tk.getTendangnhap());
            matkhautf.setText(tk.getMatkhau());
            btnOK.setEnabled(true);
            btnThem.setEnabled(false);
            btnSua.setEnabled(true);
            btnXoa.setEnabled(true);
        }
    }//GEN-LAST:event_TKTableMouseClicked


