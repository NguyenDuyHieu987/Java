    void loadTK()
    {
        TaikhoanUse use = new TaikhoanUse();
        List<TaikhoanObject> hd = use.getALL();
        IDCB.removeAllItems();
        for (TaikhoanObject nvOb : hd) {
            IDCB.addItem(String.valueOf(nvOb.getID()));
        }
    }
    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        TaikhoanObject tk = new TaikhoanObject();
        int ID = Integer.valueOf(String.valueOf(Manvtf.getText()));
        tk.setID(ID);
        tk.setTendangnhap(tendangnhaptf.getText());
        tk.setMatkhau(matkhautf.getText());
        String quyen = String.valueOf(quyencb.getSelectedItem());
        tk.setQuyen(quyen);
        
        if(TKUse.CheckADD(Integer.valueOf(String.valueOf(Manvtf.getText()))) ==1)
        {
            JOptionPane.showMessageDialog(this, "ID '"+ID+"' đã tồn tại","Lỗi", JOptionPane.ERROR_MESSAGE);
        }
        else{
            JOptionPane.showMessageDialog(this, "Thêm thành công","Thông báo", JOptionPane.INFORMATION_MESSAGE);
            TKUse.themTK(tk);
            defaultTableModel.setRowCount(0);
            setData(TKUse.getALL());
            ClearText();
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnTimkiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimkiemActionPerformed
        // TODO add your handling code here:
        int ID = Integer.valueOf( String.valueOf(IDCB.getSelectedItem()));
        TaikhoanObject tk = TKUse.getOne(ID);
        defaultTableModel.setRowCount(0);
        defaultTableModel.addRow(new Object[]{
                tk.getID(),tk.getTendangnhap(),tk.getMatkhau(),tk.getQuyen()
            });
    }//GEN-LAST:event_btnTimkiemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int row = TKTable.getSelectedRow();
        if(row == -1)
        {
            JOptionPane.showMessageDialog(this, "VUI LÒNG CHỌN DÒNG CẦN XÓA ","LỖI ",JOptionPane.ERROR_MESSAGE);
        }
        else{
            int comfirm = JOptionPane.showConfirmDialog(this, "BAN CÓ CHẮC CHẮN MUỐN XÓA KHÔNG ?");
            if(comfirm == JOptionPane.YES_OPTION)
            {
                String manv = String.valueOf(TKTable.getValueAt(row, 0));
                TKUse.XoaTK(manv);
                defaultTableModel.setRowCount(0);
                setData(TKUse.getALL());
                ClearText();
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

