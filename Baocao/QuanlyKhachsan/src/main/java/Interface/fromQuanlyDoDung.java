    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        try {
            if (rdDoDung.isSelected()) {
                if (txtTenDo.getText().equals("") || txtDonGia.getText().equals("") || (Integer) spinnerSoLuongDoDung.getValue() == 0) {
                    JOptionPane.showMessageDialog(null, "Vui lòng nhập đầy đủ thông tin", "Thông báo", JOptionPane.WARNING_MESSAGE);
                } else {

                    int confirm = JOptionPane.showConfirmDialog(null, "Bạn có chắc muốn thêm đồ dùng " + txtTenDo.getText() + " không?", "Thông báo", JOptionPane.YES_NO_OPTION);
                    if (confirm == JOptionPane.YES_OPTION) {
                        DoDungTrongPhong doDungTrongPhong = new DoDungTrongPhong();

                        doDungTrongPhong.setTenDo(txtTenDo.getText());
                        doDungTrongPhong.setDonGia(Double.valueOf(txtDonGia.getText()));
                        doDungTrongPhong.setSoLuong((Integer) spinnerSoLuongDoDung.getValue());

                        boolean check = doDungController.ThemDoDung(doDungTrongPhong);
                        if (check == true) {
                            JOptionPane.showMessageDialog(null, "Thêm thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
//                defaultTableModel.setRowCount(0);
                            clearDataTable();
                            LoadDataTable();
                            ClearTextField_Do();
                        }
                    }
                }
            } else if (rdDoDungHongmat.isSelected()) {
                if (txtMaThue.getText().equals("") || cbTendo.getSelectedItem() == "Chọn" || (Integer) spinnerSoLuongDoDungHongMat.getValue() == 0) {
                    JOptionPane.showMessageDialog(null, "Vui lòng nhập đầy đủ thông tin", "Thông báo", JOptionPane.WARNING_MESSAGE);
                } else {

                    int confirm = JOptionPane.showConfirmDialog(null, "Bạn có chắc muốn thêm đồ dùng hỏng, mất " + cbTendo.getSelectedItem()
                            + " vào mã thuê " + txtMaThue.getText() + " không?", "Thông báo", JOptionPane.YES_NO_OPTION
                    );
                    if (confirm == JOptionPane.YES_OPTION) {
                        DoDungMatHong doDungMatHong = new DoDungMatHong();

                        doDungMatHong.setTenDo(cbTendo.getSelectedItem().toString());
                        doDungMatHong.setSoLuong((Integer) spinnerSoLuongDoDungHongMat.getValue());

                        List<DoDungMatHong> listdoDoDungMatHongs = new ArrayList<>();
                        listdoDoDungMatHongs.add(doDungMatHong);
                        boolean check = doDungController.UpdateDoDungHong(listdoDoDungMatHongs, txtMaThue.getText());
                        if (check == true) {
                            JOptionPane.showMessageDialog(null, "Thêm thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
//                defaultTableModel.setRowCount(0);
                            clearDataTable();
                            LoadDataTable();
                            ClearTextField_Do();
                        }
                    }
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Sửa thất bại", "Thông báo", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnThemActionPerformed

