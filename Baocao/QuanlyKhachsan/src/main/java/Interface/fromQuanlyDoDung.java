    public void InsertDataTable() {
        doDungController = new DoDungController();
        defaultTableModelDo = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        defaultTableModelDoHongMat = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        tbDoDung.setModel(defaultTableModelDo);
        defaultTableModelDo.addColumn("Tên đồ dùng");
        defaultTableModelDo.addColumn("Đơn giá");
        defaultTableModelDo.addColumn("Số lượng");

        tbDoDungHongMat.setModel(defaultTableModelDoHongMat);
        defaultTableModelDoHongMat.addColumn("Mã thuê");
        defaultTableModelDoHongMat.addColumn("Tên đồ dùng");
        defaultTableModelDoHongMat.addColumn("Số lượng");
        defaultTableModelDoHongMat.addColumn("Tổng tiền");

        LoadDataTable();
        txtTenDo.requestFocus();
    }

    public void LoadDataTable() {
        cbMaThue.removeAllItems();
        cbTendo.addItem("Chọn");
        List<DoDungTrongPhong> listdoDoDungTrongPhongs = doDungController.GetAllDoDungTrongPhong();

        for (DoDungTrongPhong doDungTrongPhong : listdoDoDungTrongPhongs) {
            defaultTableModelDo.addRow(new Object[]{
                doDungTrongPhong.getTenDo(),
                new BigDecimal(doDungTrongPhong.getDonGia()).longValue(),
                doDungTrongPhong.getSoLuong()});

            cbTendo.addItem(doDungTrongPhong.getTenDo());
        }

        List<DoDungMatHong> listdoDoDungMatHongs = doDungController.GetAllDoDungHongMat();

        for (DoDungMatHong doDungMatHong : listdoDoDungMatHongs) {
            defaultTableModelDoHongMat.addRow(new Object[]{
                doDungMatHong.getMaThue(),
                doDungMatHong.getTenDo(),
                doDungMatHong.getSoLuong(),
                new BigDecimal(doDungMatHong.getTongTien()).longValue()});

        }

    }

    public void clearDataTable() {
        while (defaultTableModelDo.getRowCount() > 0) {
            defaultTableModelDo.removeRow(0);
        }
        while (defaultTableModelDoHongMat.getRowCount() > 0) {
            defaultTableModelDoHongMat.removeRow(0);
        }
    }

