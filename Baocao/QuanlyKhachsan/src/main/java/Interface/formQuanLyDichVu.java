    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:

        try {
            if (rdDichVu.isSelected()) {
                if (txtTenDichVu.getText().equals("") || txtDonGia.getText().equals("")) {
                    JOptionPane.showMessageDialog(null, "Vui lòng nhập đầy đủ thông tin", "Thông báo", JOptionPane.WARNING_MESSAGE);
                } else {

                    int confirm = JOptionPane.showConfirmDialog(null, "Bạn có chắc muốn thêm dịch vụ " + txtTenDichVu.getText() + " không?", "Thông báo", JOptionPane.YES_NO_OPTION);
                    if (confirm == JOptionPane.YES_OPTION) {
                        DichVu dichVu = new DichVu();

                        dichVu.setTenDichVu(txtTenDichVu.getText());
                        dichVu.setGiaTrenGio(Double.valueOf(txtDonGia.getText()));

                        boolean check = dichVuController.ThemDichVu(dichVu);
                        if (check == true) {
                            JOptionPane.showMessageDialog(null, "Thêm thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
//                defaultTableModel.setRowCount(0);
                            clearDataTable();
                            LoadDataTable();
                            ClearTextField_DichVu();
                        }
                    }
                }
            } else if (rdThueDichVu.isSelected()) {
                if (cbMaThue.getSelectedItem() == "Chọn" || cbTenDichVu.getSelectedItem() == "Chọn" || (Integer) spinnerSoGioLan.getValue() == 0) {
                    JOptionPane.showMessageDialog(null, "Vui lòng nhập đầy đủ thông tin", "Thông báo", JOptionPane.WARNING_MESSAGE);
                } else {

                    int confirm = JOptionPane.showConfirmDialog(null, "Bạn có chắc muốn thêm dịch vụ " + cbTenDichVu.getSelectedItem()
                            + " vào mã thuê  " + cbMaThue.getSelectedItem() + " không?", "Thông báo", JOptionPane.YES_NO_OPTION
                    );
                    if (confirm == JOptionPane.YES_OPTION) {
                        DichVuDaThue dichVuDaThue = new DichVuDaThue();

                        dichVuDaThue.setMaThue(cbMaThue.getSelectedItem().toString());
                        dichVuDaThue.setTenDichVu(cbTenDichVu.getSelectedItem().toString());
                        dichVuDaThue.setSoGio((Integer) spinnerSoGioLan.getValue());

                        boolean check = dichVuController.ThemDichVuDaThue(dichVuDaThue);
                        if (check == true) {
                            JOptionPane.showMessageDialog(null, "Thêm thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
//                defaultTableModel.setRowCount(0);
                            clearDataTable();
                            LoadDataTable();
                            ClearTextField_DichVuDaThue();
                        }
                    }
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Thêm thất bại", "Thông báo", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnThemActionPerformed

