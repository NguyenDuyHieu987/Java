    public void InsertDataTable() {
        dichVuController = new DichVuController();
        thueTraController = new ThueTraController();

        defaultTableModelDichVu = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        defaultTableModelDichVuDaThue = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        tbDichVu.setModel(defaultTableModelDichVu);
        defaultTableModelDichVu.addColumn("Tên dịch vụ");
        defaultTableModelDichVu.addColumn("Giá trên giờ (lần)");
        tbDichVu.getColumnModel().getColumn(0).setPreferredWidth(150);

        tbDichVuDaThue.setModel(defaultTableModelDichVuDaThue);
        defaultTableModelDichVuDaThue.addColumn("Mã thuê");
        defaultTableModelDichVuDaThue.addColumn("Tên dịch vụ");
        defaultTableModelDichVuDaThue.addColumn("Số giờ (lần)");
        defaultTableModelDichVuDaThue.addColumn("Tổng tiền");
        tbDichVuDaThue.getColumnModel().getColumn(1).setPreferredWidth(150);

        LoadDataTable();
        txtTenDichVu.requestFocus();
    }

    public void LoadDataTable() {

        cbMaThue.removeAllItems();
        cbMaThue.addItem("Chọn");
        List<ThueTraPhong> listthuThueTraPhongs = thueTraController.SearchThue_ChuaThanhToan();

        for (ThueTraPhong thueTraPhong : listthuThueTraPhongs) {

            cbMaThue.addItem(thueTraPhong.getMaThue());
        }

        cbTenDichVu.removeAllItems();
        cbTenDichVu.addItem("Chọn");
        List<DichVu> listDichVus = dichVuController.GetAllDichVu();

        for (DichVu dichVu : listDichVus) {
            defaultTableModelDichVu.addRow(new Object[]{
                dichVu.getTenDichVu(),
                new BigDecimal(dichVu.getGiaTrenGio()).longValue(),});

            cbTenDichVu.addItem(dichVu.getTenDichVu());
        }

        List<DichVuDaThue> listdiDichVuDaThues = dichVuController.GetAllDichVuDaThue();

        for (DichVuDaThue dichVuDaThue : listdiDichVuDaThues) {
            defaultTableModelDichVuDaThue.addRow(new Object[]{
                dichVuDaThue.getMaThue(),
                dichVuDaThue.getTenDichVu(),
                dichVuDaThue.getSoGio(),
                new BigDecimal(dichVuDaThue.getTongTien()).longValue(),});
        }

        cbMaThueSearch.removeAllItems();
        List<DichVuDaThue> listdiDichVuDaThues1 = dichVuController.GetAllDichVuDaThue_MaThue();

        for (DichVuDaThue dichVuDaThue : listdiDichVuDaThues1) {
            cbMaThueSearch.addItem(dichVuDaThue.getMaThue());

        }

    }

    public void clearDataTable() {
        while (defaultTableModelDichVu.getRowCount() > 0) {
            defaultTableModelDichVu.removeRow(0);
        }
        while (defaultTableModelDichVuDaThue.getRowCount() > 0) {
            defaultTableModelDichVuDaThue.removeRow(0);
        }
    }

