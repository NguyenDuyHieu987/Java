    private void tbDichVuDaThueMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbDichVuDaThueMouseClicked
        // TODO add your handling code here:
        defaultTableModelDichVuDaThue = (DefaultTableModel) tbDichVuDaThue.getModel();
        int row = tbDichVuDaThue.getSelectedRow();

        String MaThue = defaultTableModelDichVuDaThue.getValueAt(row, 0).toString();

        List<DichVuDaThue> listdiDichVuDaThues = dichVuController.GetAllDichVuDaThue_MaThue();
        for (DichVuDaThue dichVuDaThue : listdiDichVuDaThues) {
            if (listdiDichVuDaThues.isEmpty()) {
                cbMaThue.setSelectedIndex(0);

            } else {
                if (MaThue.equals(dichVuDaThue.getMaThue())) {
                    cbMaThue.setSelectedItem(MaThue);
                }
            }

        }

        String DichVu = defaultTableModelDichVuDaThue.getValueAt(row, 1).toString();
        switch (DichVu) {

            case "Dặt ủi và dọn dẹp phòng":
                cbTenDichVu.setSelectedItem("Dặt ủi và dọn dẹp phòng");
                break;
            case "Giữ trẻ":
                cbTenDichVu.setSelectedItem("Giữ trẻ");
                break;
            case "Karaoke":
                cbTenDichVu.setSelectedItem("Karaoke");
                break;
            default:
                cbTenDichVu.setSelectedIndex(0);
                break;
        }
        spinnerSoGioLan.setValue(defaultTableModelDichVuDaThue.getValueAt(row, 2));
    }//GEN-LAST:event_tbDichVuDaThueMouseClicked

