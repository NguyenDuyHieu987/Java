    private void btnNhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNhapActionPerformed
        // TODO add your handling code here:
        int a = Integer.parseInt(txtNhap.getText());
        if (cbSoAm.isSelected()) {
            if (!txtNhap.getText().equals("")) {
                defaultListModel.addElement(a);
                listNumber.setModel(defaultListModel);
            }
        } else {
            if (a >= 0) {
                defaultListModel.addElement(a);
                listNumber.setModel(defaultListModel);
            }
        }

    }//GEN-LAST:event_btnNhapActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        List<Integer> list = new ArrayList<>();
        for (int i = 0; i < listNumber.getModel().getSize(); i++) {
            list.add(listNumber.getModel().getElementAt(i));
        }

        System.out.println(list.size());

        int[] select = {};
        for (int i = 0; i < list.size(); i++) {
            if (list.get(i) % 2 == 0) {
                select = Arrays.copyOf(select, select.length + 1);
                select[select.length - 1] = i;
            }
        }
        listNumber.setSelectedIndices(select);

    }//GEN-LAST:event_jButton1ActionPerformed

