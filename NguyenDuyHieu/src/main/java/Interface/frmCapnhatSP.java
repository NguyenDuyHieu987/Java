    private void tbSanPhamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbSanPhamMouseClicked
        // TODO add your handling code here:

        TableModel tableModel = tbSanPham.getModel();
        int row = tbSanPham.getSelectedRow();
        txtMaSP.setText(tableModel.getValueAt(row, 0).toString());
        txtTenSP.setText(tableModel.getValueAt(row, 1).toString());
        txtGiaBan.setText(tableModel.getValueAt(row, 2).toString());
        String maLoai = tableModel.getValueAt(row, 3).toString();
        switch (maLoai) {
            case "Bánh kẹo":
                cbMaLoaiSP.setSelectedIndex(0);
                break;
            case "Giải khát":
                cbMaLoaiSP.setSelectedIndex(1);
                break;
            case "Mỹ phẩm":
                cbMaLoaiSP.setSelectedIndex(2);
                break;
        }

    }//GEN-LAST:event_tbSanPhamMouseClicked

