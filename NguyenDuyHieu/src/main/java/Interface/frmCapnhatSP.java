    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:

        try {
            if (txtMaSP.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Vui lòng chọn 1 sản phẩm để xóa", "Thông báo", JOptionPane.WARNING_MESSAGE);
            } else {
                if (txtMaSP.getText().length() > 4 || txtTenSP.getText().length() > 20) {
                    JOptionPane.showMessageDialog(null, "Mã sản phẩm chỉ là 4 ký tự, tên sản phẩm chỉ 20 ký tự", "Thông báo", JOptionPane.WARNING_MESSAGE);
                } else {
                    int confirm = JOptionPane.showConfirmDialog(null, "Bạn có chắc muốn xóa sản phẩm " + txtTenSP.getText() + "không", "Thông báo", JOptionPane.YES_NO_OPTION);
                    if (confirm == JOptionPane.YES_OPTION) {
                        boolean check = sanPhamController.XoaSP(txtMaSP.getText());
                        if (check == true) {
                            JOptionPane.showMessageDialog(null, "Xóa thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
//                defaultTableModel.setRowCount(0);
                            clearDataTable();
                            LoadDataTable();
                            ClearTextField();
                            setTextField(true);
                            setButton(true);
                        }
                    }
                }

            }
        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(null, "Xóa thất bại", "Thông báo", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        try {

            if (txtMaSP.getText().equals("") || txtTenSP.getText().equals("") || txtGiaBan.getText().equals("")
                    || txtTenLoaiSP.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Vui lòng nhập đầy đủ thông tin", "Thông báo", JOptionPane.WARNING_MESSAGE);
            } else {

                if (txtMaSP.getText().length() > 4 || txtTenSP.getText().length() > 20) {
                    JOptionPane.showMessageDialog(null, "Mã sản phẩm chỉ là 4 ký tự, tên sản phẩm chỉ 20 ký tự", "Thông báo", JOptionPane.WARNING_MESSAGE);
                } else {
                    int confirm = JOptionPane.showConfirmDialog(null, "Bạn có chắc muốn lưu không", "Thông báo", JOptionPane.YES_NO_OPTION);
                    if (confirm == JOptionPane.YES_OPTION) {
                        SP sp = new SP();
                        sp.setMaSP(txtMaSP.getText());
                        sp.setTenSP(txtTenSP.getText());
                        sp.setDonGia(Float.valueOf(txtGiaBan.getText()));
                        sp.setMaLoai(cbMaLoaiSP.getSelectedItem().toString());

                        boolean check;
                        if (isUpdate == true) {
                            check = sanPhamController.ThemSP(sp);

                        } else {
                            check = sanPhamController.SuaSP(sp);
                        }
                        if (check == true) {
                            JOptionPane.showMessageDialog(null, "Cập nhật thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                            //                defaultTableModel.setRowCount(0);
                            clearDataTable();
                            LoadDataTable();
                            ClearTextField();
                        }

                    }

                    setTextField(false);
                    setButton(true);
                }
            }
        } catch (HeadlessException | NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Cập nhật thất bại", "Thông báo", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_btnLuuActionPerformed

