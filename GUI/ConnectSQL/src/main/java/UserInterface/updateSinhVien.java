    public void LoadDataTable() {
        List<SinhVien> lstSV = svController.GetAllSinhVien();

        for (SinhVien sv : lstSV) {
            defaultTableModel.addRow(new Object[]{
                sv.getMaSV(),
                sv.getHoTenSV(),
                sv.getLop(),
                sv.getGioiTinh()

            }
            );
        }
    }

    public void ClickTable() {
        ListSelectionModel listSelectionModel = tbSinhVien.getSelectionModel();

        listSelectionModel.setSelectionMode(listSelectionModel.SINGLE_INTERVAL_SELECTION);

        listSelectionModel.addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                int[] row = tbSinhVien.getSelectedRows();

                txtMaSV.setText(String.valueOf(tbSinhVien.getValueAt(row[0], 0)));
            }
        });
    }

    public void clearDataTable() {
        while (defaultTableModel.getRowCount() > 0) {
            defaultTableModel.removeRow(0);
        }
    }

