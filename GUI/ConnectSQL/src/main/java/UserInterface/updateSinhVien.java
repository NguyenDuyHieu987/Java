    public int convertselectItem() {
        if (rdMale.isSelected()) {
            return 1;
        } else if (rdFemale.isSelected()) {
            return 0;
        }
        return 1;
    }
    private void btnThemSV1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemSV1ActionPerformed
        // TODO add your handling code here:
        if (txtMaSV.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Vui lòng tối thiểu nhập mã sinh viên", "Thông báo", JOptionPane.WARNING_MESSAGE);
        } else {
            SinhVien sv = new SinhVien(txtMaSV.getText(), txtTen.getText(),
                    cbLop.getSelectedItem().toString(), convertselectItem());
            boolean check = svController.addSinhVien(sv);

            if (check == true) {
                JOptionPane.showMessageDialog(null, "Thêm mới thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
//                defaultTableModel.setRowCount(0);
                clearDataTable();
                LoadDataTable();

            } else {
                JOptionPane.showMessageDialog(null, "Thêm mới thất bại", "Thông báo", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_btnThemSV1ActionPerformed

    private void btnXoaSVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaSVActionPerformed
        // TODO add your handling code here:
        int confirm = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn xóa không?", "Thông báo", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {

            boolean check = svController.deleteSinhVien(txtMaSV.getText());
            if (check == true) {
                JOptionPane.showMessageDialog(null, "Xóa thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                defaultTableModel.setRowCount(0);
                LoadDataTable();

            } else {
                JOptionPane.showMessageDialog(null, "Xóa thất bại", "Thông báo", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_btnXoaSVActionPerformed

    private void btnSuaSVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaSVActionPerformed
        // TODO add your handling code here:

        int confirm = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn sửa không?", "Thông báo", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {

            SinhVien sv = new SinhVien(txtMaSV.getText(), txtTen.getText(),
                    cbLop.getSelectedItem().toString(), convertselectItem());

            boolean check = svController.updateSinhVien(sv);
            if (check == true) {
                JOptionPane.showMessageDialog(null, "Sửa thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                defaultTableModel.setRowCount(0);
                LoadDataTable();

            } else {
                JOptionPane.showMessageDialog(null, "Sửa thất bại", "Thông báo", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_btnSuaSVActionPerformed

    private void tbSinhVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbSinhVienMouseClicked
        // TODO add your handling code here:
        defaultTableModel = (DefaultTableModel) tbSinhVien.getModel();
        int row = tbSinhVien.getSelectedRow();
        txtMaSV.setText(defaultTableModel.getValueAt(row, 0).toString());
        txtTen.setText(defaultTableModel.getValueAt(row, 1).toString());
        String lop = defaultTableModel.getValueAt(row, 2).toString();
        switch (lop) {
            case "D14CNPM1":
                cbLop.setSelectedIndex(0);
                break;
            case "D14CMPM3":
                cbLop.setSelectedIndex(1);
                break;
            case "D14HTTMDT1":
                cbLop.setSelectedIndex(2);
                break;
            case "D14TTNT&TGMT":
                cbLop.setSelectedIndex(3);
                break;
            case "D14QT&ANM":
                cbLop.setSelectedIndex(4);
                break;
        }
        int cbGT = (int) defaultTableModel.getValueAt(row, 3);
        if (cbGT == 1) {
            rdMale.setSelected(true);
        } else {
            rdFemale.setSelected(true);
        }

    }//GEN-LAST:event_tbSinhVienMouseClicked

