    private void btnDangNhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDangNhapActionPerformed
        // TODO add your handling code here:
        try {
            DangNhapController DN = new DangNhapController();

            ThongTinDangNhap ttDN = new ThongTinDangNhap(txtTK.getText(), txtMK.getText());
            boolean checkLogin = DN.DangNhapHeThong(ttDN);
            if (checkLogin == true) {
                JOptionPane.showMessageDialog(null, "Đăng Nhập thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);

                MainMenu mainMenu = new MainMenu();
                mainMenu.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Đăng Nhập thất bại", "Thông báo", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage(), "Thông báo", JOptionPane.OK_CANCEL_OPTION);
        }
    }//GEN-LAST:event_btnDangNhapActionPerformed

