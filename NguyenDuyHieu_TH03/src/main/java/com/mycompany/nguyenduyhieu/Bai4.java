    private void btnGiaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGiaiActionPerformed
        // TODO add your handling code here:
        float a = Float.parseFloat(txtta.getText());
        float b = Float.parseFloat(txtb.getText());
        float c = Float.parseFloat(txtc.getText());
        if (a == 0) {
            if (b == 0) {
                if (c == 0) {
                    txtKQ.setText("Phuong trinh vo so nghiem");
                } else {
                    txtKQ.setText("Phuong trinh vo nghiem");
                }
            } else {
                txtKQ.setText("x = " + (-c / b));
            }
        } else {
            float delta = b * b - 4 * a * c;
            if (delta > 0) {
                txtKQ.setText("x1 = " + ((-b + Math.sqrt(delta)) / (2 * a))
                        + " ,x2 = " + ((-b - Math.sqrt(delta)) / (2 * a)));

            } else if (delta == 0) {
                txtKQ.setText("x = " + (-b / (2 * a)));

            } else {
                txtKQ.setText("Phuong trinh vo nghiem");
            }

        }

    }//GEN-LAST:event_btnGiaiActionPerformed

